    static let ypRed = #colorLiteral(red: 0.9771148562, green: 0.5101671815, blue: 0.4975126386, alpha: 1)
    static let ypGreen = #colorLiteral(red: 0.3764705882, green: 0.7607843137, blue: 0.5568627451, alpha: 1)
    static let ypBlack = #colorLiteral(red: 0.1019607843, green: 0.1058823529, blue: 0.1333333333, alpha: 1)
    static let ypWhite = #colorLiteral(red: 1, green: 1, blue: 1, alpha: 1)
}

