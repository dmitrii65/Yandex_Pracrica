import UIKit

extension UIFont {
    
    static func ysDisplayMedium() -> UIFont? {
        return UIFont.init(name: "YSDisplay-Medium", size: 20)
    }
    
    static func ysDisplayBold() -> UIFont? {
        return UIFont.init(name: "YSDisplay-Bold", size: 23)
    }
}

